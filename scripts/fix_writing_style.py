# 全角括弧→半角括弧変換は不要、JSONの原文と同じ括弧形式を使う。
# ただし上記マッピングは括弧形式が原文と異なる場合があるので、JSON内の括弧をマッチする際は原文どおり「（）」を使う必要がある。
# このスクリプトでは置換キーで「（）」「()」両方試す保険を入れる。


def main():
    total_changed = 0
    for slug, pairs in REPLACEMENTS.items():
        p = DETAIL_DIR / f"{slug}.json"
        if not p.is_file():
            print(f"[!] {slug}.json が無い")
            continue
        d = json.loads(p.read_text(encoding="utf-8"))
        changed = 0
        for s in d.get("sections", []):
            body = s.get("body")
            if not isinstance(body, list):
                continue
            for i, item in enumerate(body):
                if not isinstance(item, str):
                    continue
                for old, new in pairs:
                    # 全角括弧/半角括弧を入れ替えた両方試す
                    candidates = [old, old.replace("(", "(").replace(")", ")")]
                    for cand in candidates:
                        if cand in item:
                            body[i] = item.replace(cand, new)
                            changed += 1
                            break
        if changed:
            total_changed += 1
            p.write_text(
                json.dumps(d, ensure_ascii=False, indent=2),
                encoding="utf-8",
            )
            print(f"✅ {slug}: {changed}箇所書き換え")
        else:
            print(f"❌ {slug}: 該当文字列なし（既に変換済みか文言が違う可能性）")

    print(f"\n=== 完了: {total_changed} 機種を書き換え ===")


if __name__ == "__main__":
    main()
